package beanpack;

public class Student {
	private int rollno;
	private String name;
	
	public int getRollno()
	{
		return rollno;
	}
	public void setRollno(int n)
	{
		rollno=n;
	}
	public String getName()
	{
		return name;
	}
	public void setName(String n)
	{
		name=n;
	}

}
